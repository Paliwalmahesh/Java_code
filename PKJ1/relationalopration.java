package PKJ1;

public class relationalopration {

	public static void main(String[] args) {
		int a = 50;
		int b = 25;

		System.out.println(a == b);
		System.out.println(a != b);
		System.out.println(a > b);
		System.out.println(a < b);
		System.out.println(a >= b);
		System.out.println(a <= b);


	}

}
